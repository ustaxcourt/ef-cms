import { caseDetailHelper as caseDetailHelperComputed } from '../../src/presenter/computeds/caseDetailHelper';
import { runCompute } from 'cerebral/test';
import { withAppContextDecorator } from '../../src/withAppContext';

export const docketClerkViewsAutoGeneratedCaseDeadline = ({
  cerebralTest,
  expectedDeadlineDescription,
}) => {
  return it('Docket clerk views autogenerated case deadline', async () => {
    await cerebralTest.runSequence('gotoCaseDetailSequence', {
      docketNumber: cerebralTest.docketNumber,
    });

    const caseDetailHelper = withAppContextDecorator(caseDetailHelperComputed);
    const caseDetail = runCompute(caseDetailHelper, {
      state: cerebralTest.getState(),
    });

    expect(caseDetail.caseDeadlines[0].deadlineDate).toEqual(
      '2050-02-02T05:00:00.000Z',
    );
    expect(caseDetail.caseDeadlines[0].description).toEqual(
      expectedDeadlineDescription,
    );
  });
};
