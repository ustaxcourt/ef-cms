import { PETITION_TYPES } from '@shared/business/entities/EntityConstants';
import { state } from '@web-client/presenter/app.cerebral';

export type InitialPetitionFormState = {
  petitionFacts: string[];
  petitionReasons: string[];
  petitionType: string;
  filingType?: string;
};

export const setupPetitionStateAction = ({ store }: ActionProps) => {
  const initialPetitionForm: InitialPetitionFormState = {
    filingType: undefined,
    petitionFacts: [''],
    petitionReasons: [''],
    petitionType: PETITION_TYPES.autoGenerated,
  };

  store.set(state.form, initialPetitionForm);
};
